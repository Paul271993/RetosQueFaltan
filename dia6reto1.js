function calculadora(operacion,n1,n2) {
var result ;
if (operacion == "sum"){
   return result = n1 + n2;
} else if (operacion == "subs"){
   return result = n1 - n2;
} else if (operacion == "mult"){
   return result = n1 * n2;
} else if (operacion == "div")
   return result = n1 / n2;}


